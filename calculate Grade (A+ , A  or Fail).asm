ORG 100H

MOV AH,1
INT 21H

SUB AL,48

CMP AL,8
JGE APLUS

CMP AL,5
JGE A_GRADE

JMP FAIL

APLUS:
CALL NEWLINE
MOV DX,OFFSET MSG1
MOV AH,9
INT 21H
JMP EXIT

A_GRADE:
CALL NEWLINE
MOV DX,OFFSET MSG2
MOV AH,9
INT 21H
JMP EXIT

FAIL:
CALL NEWLINE
MOV DX,OFFSET MSG3
MOV AH,9
INT 21H

EXIT:
MOV AH,4CH
INT 21H

; -------- NEWLINE PROCEDURE --------
NEWLINE PROC
    MOV DL,0DH
    MOV AH,02H
    INT 21H

    MOV DL,0AH
    INT 21H
    RET
NEWLINE ENDP

MSG1 DB 'A+$'
MSG2 DB 'A$'
MSG3 DB 'Fail$'
